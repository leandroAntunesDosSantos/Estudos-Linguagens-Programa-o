public class OperadoresRelacionais {
    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 5;

        System.out.println("Igual: " + (numero1 == numero2));
        System.out.println("Diferente: " + (numero1 != numero2));
        System.out.println("Maior: " + (numero1 > numero2));
        System.out.println("Menor: " + (numero1 < numero2));
        System.out.println("Maior ou igual: " + (numero1 >= numero2));
        System.out.println("Menor ou igual: " + (numero1 <= numero2));
    }
}

