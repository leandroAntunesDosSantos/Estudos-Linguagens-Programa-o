public class OperadoresLogicos {
    public static void main(String[] args) {
        boolean verdadeiro = true;
        boolean falso = false;

        System.out.println("AND: " + (verdadeiro && falso));
        System.out.println("OR: " + (verdadeiro || falso));
        System.out.println("NOT: " + (!verdadeiro));
    }
}
